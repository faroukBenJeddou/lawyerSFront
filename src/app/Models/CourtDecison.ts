export enum CourtDecision {
  ADJOURNED,        // The hearing is adjourned to a later date
  GUILTY,           // The defendant is found guilty
  NOT_GUILTY,       // The defendant is found not guilty
  DISMISSED,        // The case is dismissed
  PENDING,          // A decision is pending
  SETTLED,          // The case is settled out of court
  APPEAL_GRANTED,    // An appeal is granted
  APPEAL_DENIED      // An appeal is denied
}
